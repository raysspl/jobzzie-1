class OpeningsController < ApplicationController
  # GET /openings - list all openings for a specific employer
  def index
  end

  # GET /openings/:id - show a specific opening for a specific employer
  def show
  end

  # GET /openings/new - form to add an opening for a specific employer
  def new
  end

  # POST /openings - create a new opening and redirect to show
  def create
  end

  # GET /openings/:id/edit - form to update an opening for a specific employer
  def edit
  end

  # PUT/PATCH /openings/:id - update an opening and redirect to show
  def update
  end

  # DELETE /openings/:id - destroy an opening and redirect to index
  def destroy
  end
end
